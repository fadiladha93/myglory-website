const languageSwitcher = document.querySelector("#language-switcher");
const menuToggle = document.querySelector(".menu-toggle");
const navPanel = document.querySelector(".nav-panel");
const dropdowns = document.querySelectorAll(".dropdown");

function closeDropdowns(exceptDropdown) {
  dropdowns.forEach((dropdown) => {
    if (dropdown !== exceptDropdown) {
      dropdown.classList.remove("open");
      dropdown.querySelector(".dropdown-toggle")?.setAttribute("aria-expanded", "false");
    }
  });
}

function translatePage(language) {
  const dictionary = translations[language] || translations.en;
  document.documentElement.lang = language;
  document.documentElement.dir = language === "ar" ? "rtl" : "ltr";
  document.body.dir = language === "ar" ? "rtl" : "ltr";

  document.querySelectorAll("[data-i18n]").forEach((element) => {
    const key = element.getAttribute("data-i18n");
    if (dictionary[key]) {
      element.textContent = dictionary[key];
    }
  });

  document.querySelectorAll("[data-i18n-placeholder]").forEach((element) => {
    const key = element.getAttribute("data-i18n-placeholder");
    if (dictionary[key]) {
      element.setAttribute("placeholder", dictionary[key]);
    }
  });

  localStorage.setItem("myglory-language", language);
}

const savedLanguage = localStorage.getItem("myglory-language") || "en";
const activeLanguage = translations[savedLanguage] ? savedLanguage : "en";
translatePage(activeLanguage);

if (languageSwitcher) {
  languageSwitcher.value = activeLanguage;

  languageSwitcher.addEventListener("change", (event) => {
    translatePage(event.target.value);
  });
}

if (menuToggle && navPanel) {
  menuToggle.addEventListener("click", () => {
    const isOpen = navPanel.classList.toggle("open");
    menuToggle.setAttribute("aria-expanded", String(isOpen));

    if (!isOpen) {
      closeDropdowns();
    }
  });
}

dropdowns.forEach((dropdown) => {
  const dropdownToggle = dropdown.querySelector(".dropdown-toggle");

  if (dropdownToggle) {
    dropdownToggle.addEventListener("click", (event) => {
      event.stopPropagation();
      const isOpen = dropdown.classList.toggle("open");
      dropdownToggle.setAttribute("aria-expanded", String(isOpen));
      closeDropdowns(dropdown);
    });
  }
});

document.addEventListener("click", (event) => {
  if (!event.target.closest(".dropdown")) {
    closeDropdowns();
  }
});

document.addEventListener("keydown", (event) => {
  if (event.key === "Escape") {
    closeDropdowns();
  }
});
